def bfs(data, start):
    q = [[start]]
    seen = set([start])
    while q:
        print("Q:", q)
        path = q.pop(0)
        print("Path:", path)
        print("Q:", q)
        curr = path[-1]
        if curr == goal:
            print("DEST")
            return path
        row, col = curr
        for n_row, n_col in [
            (row, col + 1),
            (row, col - 1),
            (row + 1, col),
            (row - 1, col),
        ]:
            safe = is_safe(row, col, n_row, n_col, data)
            cache = (n_row, n_col) not in seen
            if safe and cache:
                q.append(path + [(n_row, n_col)])
                seen.add((n_row, n_col))


