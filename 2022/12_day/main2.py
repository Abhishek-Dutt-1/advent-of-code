def is_safe(row, col, n_row, n_col, data):
    # print(row, col, "=>", n_row, n_col)
    # if 0 > n_row >= rows or 0 > n_col >= cols:
    if n_row < 0 or n_row >= rows:
        # print(f"{n_row} row OOB")
        return False
    if n_col < 0 or n_col >= cols:
        # print(f"{n_col} col OOB")
        return False

    curr = data[row][col]
    next = data[n_row][n_col]
    if curr == "S":
        curr = "a"
    if next == "E":
        next = "z"

    if ord(next) - ord(curr) > 1:
        # print(f"{n_row}, {n_col} TOO HIGH")
        return False

    return True


